// Regenerates src/engine/brushData.ts by base64-inlining the brush SVGs so the
// published package is self-contained (no asset files to copy/serve).
// Run: npm run gen:brushes
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const SVGS = ["public/brushes/stroke1.svg", "public/brushes/stroke2.svg"];

const uris = SVGS.map((rel) => {
  const b64 = readFileSync(join(root, rel)).toString("base64");
  return `data:image/svg+xml;base64,${b64}`;
});

const out =
  `// AUTO-GENERATED by scripts/gen-brush-data.mjs — do not edit by hand.\n` +
  `// Run \`npm run gen:brushes\` to regenerate from public/brushes/*.svg.\n` +
  `// The brush ink lives in the SVG's alpha and is recoloured per-stroke in the\n` +
  `// fragment shader; inlining keeps the package dependency-free of static assets.\n\n` +
  `export const BRUSH_DATA_URIS: string[] = [\n` +
  uris.map((u) => `  "${u}",`).join("\n") +
  `\n];\n`;

writeFileSync(join(root, "src/engine/brushData.ts"), out);
console.log(
  `Wrote src/engine/brushData.ts (${uris.length} brushes, ${out.length} bytes).`,
);
